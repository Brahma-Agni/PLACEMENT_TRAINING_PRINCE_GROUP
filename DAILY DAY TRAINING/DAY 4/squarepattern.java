
public class squarepattern{
    public static void main(String[] args){
        System.out.println("\n\n\n");
        int n = 3;
        for(int i = 0; i<n ; i++)
        {
            for(int j = 0; j<n; j++){
                System.out.printf("*");
            }
            System.out.println();
        }
        

    }
}